// Función para obtener el idioma actual
function getCurrentLanguage() {
    const path = window.location.pathname;
    if (path.startsWith('/en/')) return 'en';
    if (path.startsWith('/fr/')) return 'fr';
    return 'es'; // Español por defecto
}

// Función para cambiar idioma
function changeLanguage(lang) {
    const currentPath = window.location.pathname;
    let newPath;
    
    if (lang === 'es') {
        // Remover prefijo de idioma si existe
        newPath = currentPath.replace(/^\/(en|fr)\//, '/');
    } else {
        // Agregar o cambiar prefijo de idioma
        newPath = currentPath.replace(/^\/(en|fr)\//, `/${lang}/`);
        if (!newPath.startsWith(`/${lang}/`)) {
            newPath = `/${lang}${newPath}`;
        }
    }
    
    window.location.href = newPath;
}

